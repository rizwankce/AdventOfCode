struct Point: CustomStringConvertible, Equatable, Hashable {
    var x: Int
    var y: Int

    var description: String {
        get {
            return "X: \(self.x) Y: \(self.y)"
        }
    }

    mutating func move(_ x: Int = 3, y: Int = 1) {
        self.x = x
        self.y = y
    }

    mutating func move(in direction: Character?) {
        switch direction {
        case "L": x -= 1
        case "R": x += 1
        case "U": y -= 1
        case "D": y += 1
        default:
            fatalError()
        }
    }
}

var grid: [Point: String] = [:]
var maxX: Int = input.count
var maxY: Int = input[0].count

input.enumerated().forEach { (i,line) in
    line.enumerated().forEach { (j,char) in
        let p = Point.init(x: i, y: j)
        grid[p] = String(char)
    }
}

print(grid)
print("max x : \(maxY)")
print("max y : \(maxY)")

func treesEncountered(_ r: Int, _ l: Int ) -> Int {
    var result = 0
    var p = Point.init(x: 0, y: 0)

    while true {
        p.x = p.x + l
        p.y = p.y + r
        if p.x >= maxX {
            break
        }

        if let v = grid[p] {
            if v == "#" {
                result += 1
            }
        }
        else {
            p.y = p.y - maxY
            let v = grid[p]
            if v == "#" {
                result += 1
            }
        }
    }

    return result
}

func partOne() -> Int {
    return treesEncountered(3, 1)
}

func partTwo() -> Int {
    return treesEncountered(1, 1) * treesEncountered(3, 1) * treesEncountered(5, 1) * treesEncountered(7, 1) * treesEncountered(1, 2)
}


print("Part One Answer is: \(partOne())")
print("Part Two Answer is: \(partTwo())")





