struct Tile: CustomStringConvertible, Equatable, Hashable {
    var row: Int
    var col: Int

    var description: String {
        get {
            return "Row: \(self.row) Col: \(self.col)"
        }
    }
}
typealias Image = [Tile: String]

var camera: [Int: Image] = [:]
var maxX: Int = 9
var maxY: Int = 9

for tile in input {
    let t = tile.components(separatedBy: .newlines)
    let id = Int(t[0].split(separator: " ")[1].dropLast())!
    var image: [Tile: String] = [:]
    t[1...].enumerated().forEach { (i,r) in
        r.enumerated().forEach { (j, c) in
            image[Tile.init(row: i, col: j)] = String(c)
        }
    }
    camera[id] = image
}

func borders(for image: Image) -> [[String]] {
    var b: [[String]] = []
    var side: [String] = []
    (0..<10).forEach { i in
        let t = Tile.init(row: 0, col: i)
        side.append(image[t]!)
    }
    b.append(side)
    b.append(side.reversed())
    side.removeAll()
    (0..<10).forEach { i in
        let t = Tile.init(row: i, col: 0)
        side.append(image[t]!)
    }
    b.append(side)
    b.append(side.reversed())
    side.removeAll()
    (0..<10).forEach { i in
        let t = Tile.init(row: maxX, col: i)
        side.append(image[t]!)
    }
    b.append(side)
    b.append(side.reversed())
    side.removeAll()
    (0..<10).forEach { i in
        let t = Tile.init(row: i, col: maxY)
        side.append(image[t]!)
    }
    b.append(side)
    b.append(side.reversed())
    return b
}

func compareBorder(_ border1: [String], _ border2: [String]) -> Bool {
    return border1.elementsEqual(border2)
}

func compareBorders(_ borders1: [[String]], _ borders2: [[String]]) -> Int {
    var result: Int = 0
    borders1.forEach { b1 in
        borders2.forEach { b2 in
            if compareBorder(b1, b2) {
                result += 1
            }
        }
    }
    return result / 2
}

func partOne() -> Int {
    var result: [Int] = []

    for tile in camera {
        let id = tile.key
        let img = tile.value
        let currentBorders = borders(for: img)
        let others = camera.filter { $0.key != id }
        var sidesMatching : Int = 0
        others.forEach { other in
            let otherImage = other.value
            let otherBorders = borders(for: otherImage)
            sidesMatching += compareBorders(currentBorders, otherBorders)
        }
        if sidesMatching == 2 {
            result.append(id)
        }
    }

    return result.reduce(1, *)
}

